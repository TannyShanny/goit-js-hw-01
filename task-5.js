"use strict";

let cost;
let country = prompt("Укажите страну доставки");
if (country == null) {
    alert("Отменено пользователем!")
}

country = country.toLowerCase();
let newCountry = country[0].toUpperCase() + country.slice(1);

const China = 100;
const Chile = 250;
const Australia = 170;
const India = 80;
const Jamaica = 120;

switch (newCountry) {
    case "Китай":
        cost = China;
        alert(`Доставка в ${newCountry} будет стоить ${cost} кредитов`);
        break;

    case "Чили":
        cost = Chile;
        alert(`Доставка в ${newCountry} будет стоить ${cost} кредитов`);
        break;

    case "Австралия":
        cost = Australia;
        alert(`Доставка в ${newCountry} будет стоить ${cost} кредитов`);
        break;

    case "Индия":
        cost = India;
        alert(`Доставка в ${newCountry} будет стоить ${cost} кредитов`);
        break;

    case "Ямайка":
        cost = Jamaica;
        alert(`Доставка в ${newCountry} будет стоить ${cost} кредитов`);
        break;
    
    default:
    alert("В вашей стране доставка недоступна");
}